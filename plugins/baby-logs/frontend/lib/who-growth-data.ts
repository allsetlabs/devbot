export function getWhoData(gender: 'male' | 'female', metric: GrowthMetric): PercentileEntry[] {
  return dataMap[gender][metric];
}

/** Estimate which percentile a value falls in at a given age in months */
export function estimatePercentile(
  gender: 'male' | 'female',
  metric: GrowthMetric,
  ageMonths: number,
  value: number
): number {
  const data = getWhoData(gender, metric);

  // Clamp and interpolate between months
  const floorMonth = Math.max(0, Math.min(24, Math.floor(ageMonths)));
  const ceilMonth = Math.min(24, floorMonth + 1);
  const frac = ageMonths - floorMonth;

  const lower = data[floorMonth];
  const upper = data[ceilMonth];

  const interp = (key: keyof PercentileEntry) =>
    (lower[key] as number) + frac * ((upper[key] as number) - (lower[key] as number));

  const p3 = interp('p3');
  const p15 = interp('p15');
  const p50 = interp('p50');
  const p85 = interp('p85');
  const p97 = interp('p97');

  // Simple linear interpolation between percentile bands
  if (value <= p3) return Math.max(1, Math.round((value / p3) * 3));
  if (value <= p15) return Math.round(3 + ((value - p3) / (p15 - p3)) * 12);
  if (value <= p50) return Math.round(15 + ((value - p15) / (p50 - p15)) * 35);
  if (value <= p85) return Math.round(50 + ((value - p50) / (p85 - p50)) * 35);
  if (value <= p97) return Math.round(85 + ((value - p85) / (p97 - p85)) * 12);
  return Math.min(99, Math.round(97 + ((value - p97) / p97) * 6));
}

